package dima.introspectionbasedagents.competences;

public class CompetenceException extends Exception {

	public CompetenceException() {
		super();
	}

	public CompetenceException(String message) {
		super(message);
	}

	public CompetenceException(Throwable cause) {
		super(cause);
	}

	public CompetenceException(String message, Throwable cause) {
		super(message, cause);
	}

}